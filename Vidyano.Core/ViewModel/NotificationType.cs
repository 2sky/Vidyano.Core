namespace Vidyano.ViewModel
{
    /// <summary>
    /// Represents the type of a Notification.
    /// </summary>
    public enum NotificationType
    {
        /// <summary>
        /// An Error notification.
        /// </summary>
        Error = 0,

        /// <summary>
        /// A Notice notification.
        /// </summary>
        Notice = 1,

        /// <summary>
        /// An OK notification.
        /// </summary>
        OK = 2,

        /// <summary>
        /// A Warning notification.
        /// </summary>
        Warning = 3,
    }
}